<p>The Sent tab lists messages whose status is 'sent'.</p>
<p>The Active tab lists messages whose status is 'submitted' 'inprocess' or 'suspended'.</p>
<p>The Draft tab lists messages whose status is 'draft'.</p>
<table>
<tr>
<td>ID</td>
<td>The message ID</td>
</tr>
<tr>
<td>Details</td>
<td>The From address, the Subject, the time that the message was created, on the Sent tab, the time the
 message was last sent, 
and, on the Draft and Active tabs, the embargo date.</td>
</tr>
<tr>
<td>Status</td>
<td>The message status.</td>
</tr>
<tr>
<td>Resend button</td>
<td>This button is shown only on the Sent tab. Displays the Resend page from where you can resend the message
 to selected subscribers.</td>
</tr>
<tr>
<td>Requeue button</td>
<td>This button is shown on the Sent and Active tabs. Requeues the selected message, setting it to queued 
status, and displays the Active tab.</td>
</tr>
<tr>
<td>Copy button</td>
<td>Creates a new message, setting the subject, from, to and replyto addresses, message content and message
 format from the selected message.
The new message is set to draft status.
</td>
</tr>
<tr>
<td>Delete button</td>
<td>Deletes the selected campaign and all related data including click tracking data, which means link clicks
 will no longer work.</td>
</tr>
<tr>
<td>Delete drafts button</td>
<td>This button is shown only on the Draft tab. Deletes all draft messages that have either an empty subject or
 the default value "(no subject)". This is the same as the "delete all draft messages without subject" command on
 the core phplist draft messages tab.</td>
</tr>
<tr>
<td>Edit button</td>
<td>Transfers to the phplist send page to edit the selected message.</td>
</tr>
</table>

